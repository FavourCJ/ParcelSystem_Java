    public void refresh_JTable() {

        List itemPath = ParcelSystem.itemPath;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[4];
        Iterator itr = itemPath.iterator();
        itemPath p;
        model.setNumRows(0); //Remove all existing rows of JTable
        //Add every element of the List Array as a new row into JTable
        while (itr.hasNext()) {
            p = (itemPath) itr.next();
            rowData[0] = p.getItemPath_id();
             rowData[1] = p.getPostOffice_code();
             rowData[2] = p.getItem_id();
              rowData[3] = p.getCus_id();
               
            model.addRow(rowData);
            
           
        }
    }

