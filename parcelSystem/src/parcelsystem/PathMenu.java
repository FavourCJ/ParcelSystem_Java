    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          if(
           !jTextField1.getText().trim().isEmpty()&&
           !jTextField2.getText().trim().isEmpty()&&
          !jTextField4.getText().trim().isEmpty()&&
           !jTextField5.getText().trim().isEmpty()) {
            int cus_id         = Integer.parseInt(jTextField5.getText().trim());
             int item_id         = Integer.parseInt(jTextField4.getText().trim());
            String itemPath_id      = jTextField1.getText().trim();
            String postOffice_code = jTextField2.getText().trim();
           
                                          
           List itemPath =ParcelSystem.itemPath; 
           itemPath path;
           Boolean found=false;
           Iterator <itemPath> itr = itemPath.iterator();
           while (itr.hasNext()) {
              path = itr.next(); 
              if(item_id==path.getItem_id()) {
                found=true;
                break;
              }
            }
           if (!found) {  
             // New Student Record  
             ParcelSystem.add_itemPath(itemPath_id,  postOffice_code,  item_id, cus_id);
             JOptionPane.showMessageDialog(null, "NEW Item path Record Successfully ADDED!");
           } 
           else {
             // found is true! So existing student recort will be updated!!!  
             ParcelSystem.edit_itemPath(itemPath_id,  postOffice_code,  item_id, cus_id);
             JOptionPane.showMessageDialog(null, "Item Path Record Successfully EDITED!");

           }
    }                                        
        else {
            JOptionPane.showMessageDialog(null, "You have to fill all fields before saving!");
        }
        refresh_JTable();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(
           !jTextField1.getText().trim().isEmpty()) {
            int item_id    = Integer.parseInt(jTextField4.getText().trim());
            
           List itemPath =ParcelSystem.itemPath; 
           itemPath path;
           Boolean found=false;
           Iterator <itemPath> itr = itemPath.iterator();
           while (itr.hasNext()) {
              path = itr.next(); 
              if(item_id==path.getItem_id()) {
                found=true;
                break;
              }
            }
           if (found) {  
             ParcelSystem.delete_itemPath(item_id);
             JOptionPane.showMessageDialog(null, "Selected customer Record Successfully DELETED!");
           } 
    } else                                      
       {
            JOptionPane.showMessageDialog(null, "You have to fill Student id field before deleting the record!");
        }
        refresh_JTable();
    }//GEN-LAST:event_jButton3ActionPerformed

